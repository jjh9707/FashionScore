from algorithms.style_matching import style_score, explain

# ──────────────────────────────────────────
# ✅ 확장 테스트 케이스 (다양화)
# ──────────────────────────────────────────

TEST_CASES = [

    # 1. 동일 스타일
    (["캐주얼"], ["캐주얼"], 90),
    (["포멀"], ["포멀"], 95),
    (["스트리트"], ["스트리트"], 85),
    (["미니멀"], ["미니멀"], 85),
    (["스포티"], ["스포티"], 80),

    # 2. 같은 계열
    (["캐주얼"], ["스트리트"], 70),
    (["캐주얼"], ["미니멀"], 65),
    (["스트리트"], ["미니멀"], 65),

    # 3. 자연스러운 조합
    (["스포티"], ["캐주얼"], 60),
    (["미니멀"], ["포멀"], 60),
    (["스트리트"], ["캐주얼"], 70),

    # 4. 애매한 조합
    (["스포티"], ["포멀"], 40),
    (["스트리트"], ["포멀"], 30),
    (["미니멀"], ["스포티"], 50),

    # 5. 충돌 조합
    (["캐주얼"], ["포멀"], 30),
    (["스트리트"], ["포멀"], 25),

    # 6. 핏 테스트
    (["오버핏"], ["슬림핏"], 85),
    (["슬림핏"], ["슬림핏"], 70),
    (["오버핏"], ["오버핏"], 65),

    # 7. 스타일 + 핏
    (["캐주얼","오버핏"], ["캐주얼","슬림핏"], 90),
    (["포멀","슬림핏"], ["포멀","슬림핏"], 95),
    (["스트리트","오버핏"], ["캐주얼","슬림핏"], 75),

    # 8. 현실 코디
    (["캐주얼","슬림핏"], ["미니멀","슬림핏"], 80),
    (["스트리트","오버핏"], ["스트리트","오버핏"], 90),
    (["미니멀","슬림핏"], ["포멀","슬림핏"], 75),
    (["스포티","오버핏"], ["캐주얼","슬림핏"], 70),

    # 9. 혼합 스타일
    (["캐주얼","스트리트"], ["캐주얼"], 85),
    (["캐주얼","미니멀"], ["스트리트"], 75),
    (["포멀","미니멀"], ["캐주얼"], 60),

    # 10. 극단 케이스
    (["포멀","슬림핏"], ["스트리트","오버핏"], 25),
    (["스포티","오버핏"], ["포멀","슬림핏"], 30),

    # 11. 예외
    ([], [], 0),
    (["캐주얼"], [], 0),
    ([], ["포멀"], 0),
]

# ──────────────────────────────────────────
# ✅ 테스트 실행
# ──────────────────────────────────────────

print("\n🔥 스타일 점수 테스트 시작\n")

total_error = 0

for t1, t2, expected in TEST_CASES:
    pred = style_score(t1, t2)
    error = abs(pred - expected)
    total_error += error

    print("======================")
    print(f"입력: {t1} + {t2}")
    print(f"예상 점수: {expected}")
    print(f"실제 점수: {pred}")
    print(f"차이: {round(error,1)}")
    print(f"설명: {explain(t1, t2)}")

print("\n======================")
print(f"🔥 평균 오차: {round(total_error/len(TEST_CASES), 2)}")
print("✅ 테스트 완료\n")